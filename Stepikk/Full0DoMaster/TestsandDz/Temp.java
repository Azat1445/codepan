//    Задача 3. «Проверка треугольника»
//
//    Цель: комбинировать арифметику, сравнения и логические операторы.
//
//    Объявите три переменные типа double — стороны a, b, c.
//
//    Сначала проверьте условие существования треугольника:
//    a + b > c && a + c > b && b + c > a.
//
//    Если условие ложно, выведите «Треугольник не существует» и завершите программу (return;).
//
//    Если треугольник существует, определите и выведите его тип:
//
//    Все три стороны равны → «Равносторонний»
//
//    Две стороны равны → «Равнобедренный»
//
//    Иначе → «Разносторонний»
//TriangleCheck.java
public class TriangleCheck {
    public static void main(String[] args) {

        /* ----- Шаг 1.  Задаём стороны ---------------------------------- */
        double a = 5;   // можно поменять на любые значения
        double b = 5;   // например, попробуйте 3-4-5 или 2-2-10
        double c = 5;

        /* ----- Шаг 2.  Проверяем существование ------------------------- */
        if (!(a + b > c && a + c > b && b + c > a)) {
            System.out.println("Треугольник не существует");
            return;                       // сразу выходим из main
        }

        /* ----- Шаг 3.  Определяем тип треугольника --------------------- */
        if (a == b && b == c) {
            System.out.println("Равносторонний");
        } else if (a == b || a == c || b == c) {
            System.out.println("Равнобедренный");
        } else {
            System.out.println("Разносторонний");
        }
    }
}
//Задача 4. «Логическая викторина»
//
//Цель: потренироваться с булевыми переменными и операторами &&, ||, !.
//
//        В main создайте три булевых переменных:
//
//    boolean isWeekend   = true;
//    boolean isSunny     = false;
//    boolean haveUmbrella = true;
//
//    Напишите условие, которое печатает:
//
//        «Иду гулять» — если сегодня выходной и (солнечно или (не солнечно, но у меня есть зонт)).
//
//        «Сижу дома» — во всех остальных случаях.
//
//Формально:
//if (isWeekend && (isSunny || (!isSunny && haveUmbrella))) …
// WalkDecision.java
public class WalkDecision {
    public static void main(String[] args) {

        /* ---------- Исходные булевы переменные ------------------------- */
        boolean isWeekend    = true;   // выходной?
        boolean isSunny      = false;  // солнечно?
        boolean haveUmbrella = true;   // есть ли зонт?

        /* ---------- Условие выхода на прогулку ------------------------- */
        // Иду гулять, если:
        //   1) выходной И
        //   2) (солнечно ИЛИ (не солнечно, но есть зонт))
        if (isWeekend && (isSunny || (!isSunny && haveUmbrella))) {
            System.out.println("Иду гулять");
        } else {
            System.out.println("Сижу дома");
        }
    }
}

